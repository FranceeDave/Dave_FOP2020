#include <iostream>

using namespace std;

int main()
{

  int angka1=12;
  int angka2=5;


  //addition
  cout << "12+5 = " << angka1 + angka2 << endl;
  
  //subtraction
  cout<<"12-5 = " << angka1 - angka2 << endl;

  //multiplication
  cout<<"12*5 = " << angka1 * angka2 << endl;

  //division
  cout<<"12/5 = " << angka1 / angka2 << endl;

  //remainder
  cout<<"12%5 = " << angka1 % angka2 << endl;
  
  return 0;
  
}